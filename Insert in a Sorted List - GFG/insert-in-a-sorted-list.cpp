class Solution{
  public:
    // Should return head of the modified linked list
    Node *sortedInsert(struct Node* head, int data) {
        // Code here
        Node *root=head;
        Node *prev=head;
        if(head->data>data){
            Node *temp=new Node(data);
            temp->next=head;
            return temp;
        }
        else{
            while(root!=NULL){
                if(root->next && root->data<=data && root->next->data>data){
                    Node *temp=new Node(data);
                    temp->next=root->next;
                    root->next=temp;
                    return head;
                }
                else if(root->next==NULL &&  root->data<=data){
                    Node *temp=new Node(data);
                    root->next=temp;
                    return head;
                }
                root=root->next;
            }
        }
        return head;
    }
};


